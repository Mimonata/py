c = int(input("Cash: "))
s = 0

while True:
	if c > 25:
		s += c // 25
		c = c % 25
	if c > 10:
		s += c // 10
		c = c % 10
	if c > 5:
		s += c // 5
		c = c % 5
	if c > 1:
		s += c // 1
		c = c % 1
	if c == 0:
		break
print(s)	
